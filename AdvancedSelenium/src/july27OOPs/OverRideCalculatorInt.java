package july27OOPs;

public class OverRideCalculatorInt extends OverRideCalculatorFloat {

	public int add(int a, int b){
		
		return (a + b);
	}
	
	public int sub(int a, int b)
	{
		return(a - b);
	}
	public int mult(int a, int b)
	{
		return(a * b);
	}
	public int div(int a, int b)
	{
		return(a / b);
	}
}
