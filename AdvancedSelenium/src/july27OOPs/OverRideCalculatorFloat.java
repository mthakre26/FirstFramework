package july27OOPs;

public class OverRideCalculatorFloat {

	
	public float sub(float a, float b)
	{
		return(a - b);
	}
	public float add(float a, float b)
	{
		return(a + b);
	}
	public float mult(float a, float b)
	{
		return(a * b);
	}
	public float div(float a, float b)
	{
		return(a / b);
	}
}
